        validateField(this, document.getElementById('phoneError'), phoneValidation, 'Please enter your phone number.');
    });

    document.getElementById('budget').addEventListener('input', function () {
        validateField(this, document.getElementById('budgetError'), budgetValidation, 'Please enter a valid budget.');
